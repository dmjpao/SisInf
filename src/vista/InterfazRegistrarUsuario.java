    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        usuario = new Usuario();  
        boolean bo = true;
        try{
            sentencia=conexion.createStatement();
            usuario.nombre = jTextFieldNombre.getText();
            usuario.contrasena = jPasswordFieldContrasena.getText();
            usuario.fecNac = jTextFieldFecNac.getText();
            usuario.fecIni = jTextFieldFecInc.getText();
            usuario.fecFin = jTextFieldFecSalida.getText();
            //usuario.rol = jTextFieldCargo.getText();
            usuario.rol = jComboBoxCargo.getSelectedItem().toString();
            //usuario.sexo = jTextFieldSexo.getText();
            usuario.sexo = jComboBoxSexo.getSelectedItem().toString();
            usuario.salario = Float.valueOf(jTextFieldSalario.getText());
            
            String sentenciaSQL = new String();
            int lastIdSexo=0, lastIdUsuario = 0, lastIdRol = 0;
            if(usuario.sexo.equals("Masculino")){
                 lastIdSexo = 2;
            }else{
                 lastIdSexo = 1;   
            }
            if(usuario.rol.equals("Encargado de Almacenes")){
                lastIdRol = 1;
            }
            if(usuario.rol.equals("Recursos Humanos")){
                lastIdRol = 2;
            }
            if(usuario.rol.equals("Administrador")){
                lastIdRol = 3;
            }
            if(usuario.rol.equals("Cajero")){
                lastIdRol = 4;
            }
            sentenciaSQL = "INSERT INTO Usuario (Sexo_idSexo,Nombre, Contrasena, FechaNacimiento, Salario)";
            sentenciaSQL = sentenciaSQL +" VALUES ("+lastIdSexo+",'"+usuario.nombre+"', '"+usuario.contrasena+"','"+usuario.fecNac+"',"+usuario.salario+") RETURNING idUsuario";
            sentencia.execute(sentenciaSQL);
            ResultSet rs = sentencia.executeQuery("select lastval()");
            if(rs.next()){
                lastIdUsuario = rs.getInt(1);
            }
            String sentenciaSQL2 = new String();
            sentenciaSQL2 = "INSERT INTO Usuario_has_Rol (Usuario_idUsuario, Rol_idRol, Activo, FechaInicio, FechaFinal)";
            sentenciaSQL2 = sentenciaSQL2 + "VALUES ("+lastIdUsuario+","+lastIdRol+","+true+",'"+usuario.fecIni+"','"+usuario.fecFin+"')";
            sentencia.execute(sentenciaSQL2);
            JOptionPane.showMessageDialog(this, "Guardado con exito");
            jTextFieldNombre.setText("");
            jPasswordFieldContrasena.setText("");
            jTextFieldFecNac.setText("");
            jTextFieldFecInc.setText("");
            jTextFieldFecSalida.setText("");
            jTextFieldSalario.setText("");
        }catch (SQLException e) {
            Logger.getLogger(InterfazRegistrarNuevoProducto.class.getName()).log(Level.SEVERE,null,e);
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

